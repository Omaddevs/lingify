export function calculateTOEFLScore(reading_correct, reading_total, section = 'reading') {
  const pct = reading_correct / reading_total
  if (pct >= 0.90) return 30
  if (pct >= 0.80) return 27
  if (pct >= 0.70) return 24
  if (pct >= 0.60) return 21
  if (pct >= 0.50) return 18
  if (pct >= 0.40) return 15
  if (pct >= 0.30) return 12
  return 9
}

export function calculateSATScore(rw_correct, rw_total, math_correct, math_total) {
  const rwPct = rw_correct / rw_total
  const mathPct = math_correct / math_total
  const rwScaled = Math.round(200 + rwPct * 600)
  const mathScaled = Math.round(200 + mathPct * 600)
  return { rw: rwScaled, math: mathScaled, total: rwScaled + mathScaled }
}

